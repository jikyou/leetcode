const fns = require(require(".")(__filename).sourcePath);

fns.forEach((fn) => {
	describe(fn.name, () => {
		it("true", () => {
			expect(
				fn([
					["5", "3", ".", ".", "7", ".", ".", ".", "."],
					["6", ".", ".", "1", "9", "5", ".", ".", "."],
					[".", "9", "8", ".", ".", ".", ".", "6", "."],
					["8", ".", ".", ".", "6", ".", ".", ".", "3"],
					["4", ".", ".", "8", ".", "3", ".", ".", "1"],
					["7", ".", ".", ".", "2", ".", ".", ".", "6"],
					[".", "6", ".", ".", ".", ".", "2", "8", "."],
					[".", ".", ".", "4", "1", "9", ".", ".", "5"],
					[".", ".", ".", ".", "8", ".", ".", "7", "9"],
				]),
			).toEqual(true);
		});

		it("false", () => {
			expect(
				fn([
					["8", "3", ".", ".", "7", ".", ".", ".", "."],
					["6", ".", ".", "1", "9", "5", ".", ".", "."],
					[".", "9", "8", ".", ".", ".", ".", "6", "."],
					["8", ".", ".", ".", "6", ".", ".", ".", "3"],
					["4", ".", ".", "8", ".", "3", ".", ".", "1"],
					["7", ".", ".", ".", "2", ".", ".", ".", "6"],
					[".", "6", ".", ".", ".", ".", "2", "8", "."],
					[".", ".", ".", "4", "1", "9", ".", ".", "5"],
					[".", ".", ".", ".", "8", ".", ".", "7", "9"],
				]),
			).toEqual(false);
		});

		it("false", () => {
			expect(
				fn([
					[".", ".", "4", ".", ".", ".", "6", "3", "."],
					[".", ".", ".", ".", ".", ".", ".", ".", "."],
					["5", ".", ".", ".", ".", ".", ".", "9", "."],
					[".", ".", ".", "5", "6", ".", ".", ".", "."],
					["4", ".", "3", ".", ".", ".", ".", ".", "1"],
					[".", ".", ".", "7", ".", ".", ".", ".", "."],
					[".", ".", ".", "5", ".", ".", ".", ".", "."],
					[".", ".", ".", ".", ".", ".", ".", ".", "."],
					[".", ".", ".", ".", ".", ".", ".", ".", "."],
				]),
			).toEqual(false);
		});
	});
});
